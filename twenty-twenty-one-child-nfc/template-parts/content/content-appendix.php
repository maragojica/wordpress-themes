                        <h2 class="section-title cl-brown pt-4 pb-4"><u>Appendix One:</u> Statement of Values</h2>
                        <p>In Jewish tradition, the guiding principle of Tikkun Olam positions justice as a pathway to repairing the world. The following values statement is rooted in an understanding of history and the ways in which philanthropy has the potential to perpetuate or disrupt a status quo of hierarchical systems that are neither sustainable nor fair. These values have been created to strengthen our ability to learn from the past, acknowledge the complexity of the present, and imagine a more just and vibrant future.</p>

