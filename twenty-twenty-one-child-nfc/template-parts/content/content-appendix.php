                        <ul>
                            <li>We recognize that diversity of thought yields superior investment performance over time.</li>

                            <li>We acknowledge the role that institutional and structural barriers play in hindering diversity and equitable access to capital. Jocelyn Ban, founder, Jocelyn Ban Consulting</li>

                            <li>We believe investors have the opportunity to transform broken systems by designing a process that is robust, inclusive, and antiracist.</li>

                            <li>We begin this work today with a series of commitments around accountability, engagement, and transparency.</li>

                        </ul>
                        <h4 class="title-info pb-4 pt-2 mb-2">Commitments:</h4>
                        <ul>
                            <li><strong>Accountability:</strong> we establish a clear definition of a diverse-led fund and require regular reporting of metrics and benchmarks to measure progress.</li>

                            <li><strong>Engagement:</strong> we engage our Board, Investment Committee, staff, consultants, and investment managers around the foundation’s commitment to building an investing process that is robust, inclusive, and antiracist.</li>

                            <li><strong>Transparency:</strong> we share our story (e.g., metrics, progress, learning) with other foundations and investment professionals, communicate selection criteria clearly, and provide constructive feedback to investment managers, including around rejections.</li>

                        </ul>
                        <p>We define diverse-led funds as those that are majority-owned by women and/or people of color. In the case of private funds, we consider only ownership among the general partners. We are initially focused on US managers but will develop an approach to measuring diversity and inclusion among managers outside of the US in time. We acknowledge that ownership is not the only relevant metric for DEI. We also recognize that our commitment to inclusivity will need to be expansive to include other communities and identities beyond women and people of color.</p>

