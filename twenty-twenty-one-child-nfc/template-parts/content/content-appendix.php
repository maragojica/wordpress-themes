                        <h4 class="title-info pb-4 pt-2">Justice and Equity</h4>
                        <p>There is a long, devastating, and sanctioned history of racialized violence and anti-Blackness that has resulted in the destruction and continued social and economic exclusion of Black, Brown, and Indigenous people and communities. Policies and practices rooted in capitalism, white supremacy, anti-Semitism, and patriarchy have worked together to prevent the vast majority of people — across identities — from reaching their full potential. We believe that philanthropy is a product of this history and that the foundation, with its power and privilege, has a responsibility to work in partnership with and be led by those individuals and communities most harmed. We are committed to investing in strategies and solutions that seek to dismantle oppressive systems and promote just, fair, and sustainable outcomes.</p>
                        <h4 class="title-info pb-4 pt-2">Interdependence</h4>
                        <p>We know that all life on this planet is interconnected and part of a broader ecosystem. We believe that prosperity can be abundant, resources shared in ways that are not extractive, and that we are more powerful when we work together toward common goals. However, these beliefs will always be in conflict with a society that operates through rugged individualism and scarcity. Therefore, we are committed to working in community with others, the equitable distribution of power and resources, and contributing to the restoration of our communities, humanity, and planet.</p>
                        <h4 class="title-info pb-4 pt-2">Learning and Listening</h4>
                        <p>We believe that no one person has all the answers. We also acknowledge the reality that all voices and types of knowledge are not valued equally. This means we must participate in spaces where we are actively listening to diverse perspectives, valuing, and compensating lived experience as expertise, and entering conversations with curiosity and humility. In this approach, we honor the work that has come before us and seek to support, build on, and credit the collective knowledge and wisdom that already exists.</p>
                        <h4 class="title-info pb-4 pt-2">Courageous Transformation</h4>
                        <p>We believe that structural change requires disruptive thinking and a bold vision for the future. The radical transformation of our society and economy relies on partnership with those whose knowledge and ideas call into question our assumptions. Even through discomfort, it is those critical partnerships that prompt us to interrogate our biases, confront systems that seek to maintain harmful power structures, and show us our potential to courageously reimagine and transform our world.</p>
                        <h4 class="title-info pb-4 pt-2">Integrity</h4>
                        <p>We believe our values are what ground our work and, in difficult times, illuminate the path forward. Clear and open lines of communication and systems of accountability will be paramount to us finding our way if and when we stray from that path. However, the presence of wealth and power can create blind spots and relationships where accountability is one way and unbalanced. We believe that shifting that balance means being transparent about our priorities and decision-making processes, being honest and open about our failures and successes, and making way for feedback with a willingness to change course to strengthen us to be better partners to the communities we serve.</p>

